public class swapping{
    public static void main(String[] args){
        int x=7;
        int y=10; int temp;
        System.out.println("Before swap");
        System.out.println("x=:"+x);
        System.out.println("y=:"+y);
        temp=x;
        x=y;
        temp=y;
        System.out.println("After swap");
        System.out.println("x=:"+x);
        System.out.println("y=:"+y);
    }

}